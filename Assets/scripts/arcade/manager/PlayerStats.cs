public class PlayerStats
{
    public int type;
    public int level;
    public int attack;
    public int health;
    public int manaCost;

    public PlayerStats(int type, int level, int attack, int health, int manaCost)
    {
        this.type = type;
        this.level = level;
        this.attack = attack;
        this.health = health;
        this.manaCost = manaCost;
    }
}
